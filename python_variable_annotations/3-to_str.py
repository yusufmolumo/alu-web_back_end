#!/usr/bin/env python3
"""
This script defines a function to convert a floating-point number to a string.

Functions:
    to_str(n): Converts a float to its string representation.
"""


def to_str(n: float) -> str:
    """
    Converts a floating-point number to its string representation.

    Args:
        n (float): The floating-point number to convert.

    Returns:
        str: The string representation of the input number.
    """
    return str(n)
